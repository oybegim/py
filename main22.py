class Odam:
    def __init__(self, ismi, familiyasi, yoshi):
        self.ismi = ismi
        self.familiyasi = familiyasi
        self.yoshi = yoshi
o1 = Odam("abc", "ABC", 1)
o2 = Odam("bcd", "BCD", 2)
o3 = Odam("cde", "CDE", 3)
print(o1.ismi, o1.familiyasi, o1.yoshi)
print(o2.ismi, o2.familiyasi, o2.yoshi)
print(o3.ismi, o3.familiyasi, o3.yoshi)

